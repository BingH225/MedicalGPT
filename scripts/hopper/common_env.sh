  DEFAULT_RM_MAX_TRAIN_SAMPLES=3000
  DEFAULT_DPO_MAX_TRAIN_SAMPLES=3000
  DEFAULT_RLOO_MAX_STEPS=80
else
  DEFAULT_SFT_MAX_TRAIN_SAMPLES=1000
  DEFAULT_RM_MAX_TRAIN_SAMPLES=500
  DEFAULT_DPO_MAX_TRAIN_SAMPLES=500
  DEFAULT_RLOO_MAX_STEPS=80
fi

SFT_MAX_TRAIN_SAMPLES="${SFT_MAX_TRAIN_SAMPLES:-${DEFAULT_SFT_MAX_TRAIN_SAMPLES}}"
